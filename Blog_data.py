import matplotlib.pyplot as plt
from matplotlib.ticker import MultipleLocator

def plot_runner_pace(runners):
  for runner, pace_data in runners.items():
    distance = list(range(1, len(pace_data) + 1))
    filtered_pace = [pace for pace in pace_data if pace != 0]
    filtered_distance = [dist for dist, pace in zip(distance, pace_data) if pace != 0]
    plt.plot(filtered_distance, filtered_pace, label=runner, linewidth=2)

  plt.xlabel("Distance (km)")
  plt.ylabel("Pace (min/km)")
  plt.gca().invert_yaxis() 
  plt.gca().xaxis.set_major_locator(MultipleLocator(1))
  plt.title("Runner Pace Comparison")
  plt.legend()
  plt.grid(True)
  plt.show()

